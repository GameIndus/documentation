<p>
    Depuis l'éditeur, il est possible de créer des cartes issues d'une image (appelée <b>tileset</b>). Ces cartes sont
    appelées des <b>tilemaps</b>.<br>
    Un éditeur permet de produire ces cartes à partir d'une image. Il se nomme <b>l'éditeur de tilemap</b>. Nous allons
    détailler dans cette partie comment l'utiliser et comment produire des cartes optimisées très rapidement.
</p>

<blockquote>
    <p>
        <i class="fa fa-info-circle"></i> Pour accéder à l'éditeur de carte, vous devez créer une tilemap depuis le menu
        "Créer un fichier". Il ne vous reste plus qu'à double-clicker sur le fichier créé pour l'ouvrir.
    </p>
</blockquote>

