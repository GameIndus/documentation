<h4>Lancement de l'éditeur</h4>

<p>
	Une fois le projet créé et configuré, il est possible de le modifier via le bouton "Ouvrir l'éditeur". 
	L'éditeur se lance ! Il est possible de trouver une explication sur son interface et son système dans un autre chapitre.
</p>