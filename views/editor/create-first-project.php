
<h4>Présentation d'un projet</h4>

<p>
	Un projet est composé d'une multitude de fichiers et de dossiers reliés entre eux. Tous les fichiers sont éditables via leurs éditeurs respectifs.<br>
	Des ressources de tout type peuvent y être liées, directement depuis certains éditeurs.<br>
	Le projet est à la base de tout jeu, et il est donc impossible de produire plusieurs jeux en un seul projet.<br>
	C'est pourquoi il est possible d'associer le terme projet au terme "jeu".
</p>

<blockquote>
	<p>
		<i class="fa fa-info-circle"></i> Pour créer un projet, inscrivez-vous sur la plateforme via <b><a target="_blank" href="http://gameindus.fr/inscription" title="Inscription">cette page</a></b>.
		<br>
		Une fois le compte confirmé, allez dans votre compte puis "Mes projets". Cliquez sur "Créer un nouveau projet".<br>
		Renseignez les informations nécessaires puis cliquez sur "Envoyer" : votre projet est maintenant créé.
	</p>
</blockquote>

