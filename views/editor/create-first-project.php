<h4>Modifier son projet</h4>

<p>
	Suite à la création d'un projet, un espace dédié à ce dernier sera crée pour l'administrer et le suivre en temps réel.
	Cette page est facilement accessible depuis la page "Mes projets".<br><br>
	Dans cet espace dédié, un menu apparaît avec de nombreuses options. Voici ci-dessous le menu de l'interface d'un projet (ici sur un projet d'exemple) : <br>

	<br>
	<img src="/imgs/create-first-project-menu.png" alt="Image du menu de l'interface d'un projet">
	<br>
	<ul>
		<li>L'onglet "Informations" permet d'accéder à une page pour voir les informations générales du projet. La liste des membres est maintenant disponible sur cette page.</li>
		<li>L'onglet "Ouvrir l'éditeur" permet de diriger l'utilisateur connecté vers l'éditeur afin de modifier le jeu/projet.</li>
		<li>L'onglet "Modifier les infos" permet de gérer les informations d'un projet et de les modifier. Cela comprend l'image de présentation, la description, et la supression dun projet.</li>
	</ul>
</p>

<blockquote class="green">
	<p>
		<i class="fa fa-info-circle"></i> Il est conseillé de bien configurer son projet avant de commencer son développement.
	</p>
</blockquote>

