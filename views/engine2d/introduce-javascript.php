<p>
	Une majorité de la plateforme GameIndus ainsi que ses composants sont développés en JavaScript. C'est un language Web maniable et puissant, qui permet
	d'animer des sites <small>(front-end)</small> et/ou de les dynamiser. C'est pourquoi nous utilisons aussi ce language dans notre moteur de jeu.<br>
	Il sera donc obligatoire de connaître ce language pour développer des scripts dans l'éditeur.
</p>

<hr>

<h4><i class="fa fa-caret-right"></i> Je ne connais pas ce language.</h4>
<p>

	Souvenez-vous, il est obligatoire pour créer ses propres scripts.<br>
	Si vous désirez apprendre ce language, nous vous conseillons ce site, très propre et complet : <a href="https://www.codecademy.com/fr/learn/javascript" target="_blank" title="CodeAcademy JavaScript">CodeAcademy JavaScript</a>.
	<br><br>
	Dans le cas contraire, vous pouvez très bien utiliser les scripts visuels, tojours en version de développement depuis l'éditeur.
</p>

<hr>

<h4><i class="fa fa-caret-right"></i> Je débute dans ce language.</h4>
<p>
	Pour concevoir un jeu avec GameIndus, vous n'avez pas besoin d'une grande base de connaissances en JavaScript. Il faudra néanmoins savoir utiliser les méthodes, les
	variables, les principales opérations (if, else, for, ...) et avoir la base de l'algorithmique. Si vous maitrîsez ces points, il n'y a aucun soucis. Dans le cas contraire,
	n'hésitez pas à vous référer à la section du dessus.
</p>

<hr>

<h4><i class="fa fa-caret-right"></i> Je le maitrise bien, mais pas à 100%.</h4>
<p>
	Vous pouvez donc vous lancer, il n'y a aucun soucis ! En effet, apprendre un language à 100% est relativement complexe, voir même impossible, car ce dernier
	ne cesse de s'améliorer, sans même que vous ne le sachiez. Si néanmoins vous ne savez pas quelles méthodes utiliser, ou si vous êtes perdu avec les
	méthodes du moteur de jeu, n'hésitez pas à aller dans les espaces dédiés.
</p>