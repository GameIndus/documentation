<p>
    Le moteur de jeu 2D de GameIndus est complet. Il embarque un grand nombre de classes, composants et librairies afin
    de
    créer au mieux vos jeux. Nous allons détailler tout ça, ici.<br>
    Voici une liste de tous les composants qu'utilise le moteur afin de fonctionner.<br>
<ul>
    <li>3 moteurs de rendus (Rendus de sprites, Rendus d'images, Rendus de formes géométriques)</li>
    <li>1 moteur physique (Physiques Box2D)</li>
    <li>Des moteurs de réseau, pour gérer la connectivité et le multijoueur</li>
    <li>1 moteur d'évenement (Input)</li>
    <li>1 moteur de comportement (Player)</li>
    <li>1 moteur de comportements personnalisés</li>
    <li>1 moteur de particules</li>
    <li>Support des textes, tilemaps, sprites, fonds, calques, animations...</li>
</ul>
